#include "libft.h"

char	*ft_strrev(char *s)
{
	int	n;
	int	i;

	n = 0;
	while (*(s + n))
		n++;
	i = 0;
	while (i < n / 2)
	{
		ft_swap(s + i, s + n - 1 - i, SIZE_CHAR);
		i++;
	}
	return (s);
}
